int	ft_sqrt(int	ab)
{	
	int	i;

	i = 2;
	if (ab <= 0)
	{
		return (0);
	}
	else
	{
		while (i <= (ab / i))
		{
			if (i * i == ab)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
