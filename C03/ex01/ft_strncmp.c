int	ft_strcmp(char	*s1, char	*s2, unsigned int n)
{
	int	i;
	int	def;

	def = 0;
	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && (i < n))
	{
		def += (s1[i] - s2[i]);
		i++;
	}
	return (def);
}
